        <h2>Code of Conduct</h2>
        <p>
            Cambridge Game Jam is committed to providing a safe, inclusive, and welcoming experience
            for everyone. This code outlines our expectations for all participants and attendees.
        </p>
        <p>
            We do not tolerate any form of harassment, discrimination, or exclusion of any form. In
            particular, we do not tolerate discrimination based on
        </p>
        <ul class="pl-8 list-disc">
            <li>Race, ethnicity, or nationality</li>
            <li>Gender identity, expression, or sexual orientation</li>
            <li>Age</li>
            <li>Disability or medical condition</li>
            <li>Physical appearance or body size</li>
            <li>Religion or belief system</li>
        </ul>
        <p>
            If you feel you are being harassed by someone else at the event, please come talk to a
            member of the committee and we will handle the situation accordingly. Participants who
            break these rules may be disqualified at the discretion of the committee.
        </p>
