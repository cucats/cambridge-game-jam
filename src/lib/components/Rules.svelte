    <h2>Rules</h2>
    <h3>Entry</h3>
    <ul class="pl-8 list-disc">
        <li>
            The maximum team size is 4. Any bigger, and we might have to split you up. Solo
            participation is allowed, but hacking is better with other people!
        </li>
        <li>
            Due to departmental rules, participants must be students at the University of Cambridge.
        </li>
    </ul>
    <h3>House rules</h3>
    <ul class="pl-8 list-disc">
        <li>
            Please do not bring food into the Intel lab! All food must stay downstairs in the
            street. &#127829;
        </li>
        <li>You must not bring or consume alcohol at the event.</li>
        <li>Staying after hours is done so at your own risk and subject to Lab opening times!</li>
        <li>
            All participants must follow our <a href="/code-of-conduct" class="theme-hyperlink"
                >Code of Conduct</a
            >.
        </li>
    </ul>
    <h3>Submission</h3>
    <ul class="pl-8 list-disc">
        <li>
            All work on projects must be done at the game jam. Using existing open source code is
            allowed if credited, but the project should be substantially new.
        </li>
        <li>
            Working on a project and open sourcing it for the sole purpose of using the code in the
            game jam is against the spirit of the rules and is not allowed.
        </li>
        <li>
            AI assistants (Copilot, ChatGPT, etc.) are allowed when writing code, but don't go
            overboard. For example, using AI assistants as a tool to help you code is fine, but
            asking one to generate a whole game or the core logic of your game is not
            allowed. Images generated by external (not part of your project) AI models must be
            credited, but such use is discouraged.
        </li>
        <li>
            After hacking finishes, representatives for each track will go around to look at each
            demo. You are free to look at other projects, as long as one team member is available to
            present a demo of their project.
        </li>
        <li>
            If you discover a breaking bug during your demo with a trivial fix, that's okay to
            rectify. &#128027;
        </li>
        <li>
            Submissions will be made to <a
                href="https://itch.io/jam/camgamejam2025"
                class="theme-hyperlink">itch.io</a
            >.
        </li>
    </ul>
</div>
